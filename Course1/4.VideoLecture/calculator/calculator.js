

const calculatorModules = {
    sum: function (sum1,sum2){
        return sum1+sum2
    },
    sub: function (sum1,sum2){
        return sum1-sum2
    },
    mul: function (sum1,sum2){
        return sum1*sum2
    },
    div: function (sum1,sum2){
        return sum1/sum2
    }
}

module.exports = calculatorModules;